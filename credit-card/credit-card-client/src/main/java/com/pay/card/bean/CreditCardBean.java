package com.pay.card.bean;

import java.io.Serializable;

public class CreditCardBean implements Serializable {

    private static final long serialVersionUID = 2235442159248693718L;
    /**
     * 商户编号
     */
    private String customerNo;
    /**
     * 手机号
     */
    private String phoneNo;
    /**
     * 渠道号
     */
    private String channel;
    /**
     * 卡Id
     */
    private Long cardId;
    /**
     * 用户id
     */
    private Long userId;

    public Long getCardId() {
        return cardId;
    }

    public String getChannel() {
        return channel;
    }

    public String getCustomerNo() {
        return customerNo;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public Long getUserId() {
        return userId;
    }

    public void setCardId(Long cardId) {
        this.cardId = cardId;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public void setCustomerNo(String customerNo) {
        this.customerNo = customerNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    @Override
    public String toString() {
        return "CreditCardBean [customerNo=" + customerNo + ", phoneNo=" + phoneNo + ", channel=" + channel
                        + ", cardId=" + cardId + ", userId=" + userId + "]";
    }

}
